""" Ejercicio 3: Escribe un programa para pedirle al usuario el número de
    horas y la tarifa por hora para calcular el salario bruto. """
horas = float(input("Introduzca el número de horas: "))
tarifa_hora = float(input("Introduce la tarifa por horas: "))
salario = horas * tarifa_hora

print('Su salario es: ', salario)
